valor = 0

# A mensagem será exibida somente uma vez
print('Executando o IF')
if valor < 5:
    print('A variavel valor é menor que 5')
    valor = valor + 1

print()
print('Executando o WHILE')
# A variavel valor vale 1 e a mensagem será exibida quantro vez
while valor < 5:
    print('A variavel valor é menor que 5')
    valor = valor + 1