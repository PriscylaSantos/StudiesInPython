# Adição
adicao  = 2 + 3
print('Adicao: 2 + 3 = ' + str(adicao))

# Subtração
subtracao = 5 - 2
print('Subtração: 5 - 2 = ' + str(subtracao))

# Multiplicação
multiplicacao = 3 * 5
print('Multiplicação: 3 * 5 = ' + str(multiplicacao))

# Divisão
divisao = 20 / 5
print('Divisão: 20 / 5 = ' + str(divisao))

# Módulo
modulo = 30 % 4
print('Módulo/Resto: 30 % 4 = ' + str(modulo))

# Divisão Inteira
divisao_inteira = 21 // 6
print('Divisão Inteita: 21 // 6 = ' + str(divisao_inteira))

# Exponencial
exponencial = 2 ** 3
print('Exponencial: 2 ** 3 = ' + str(exponencial))

# Expressão numérica:
expressao = (5 - 1) * ((7 + 1) / (3 - 1))
print('Expressao numérica: (5 - 1) + ((7 + 1) / (3 - 1)) = ' + str(expressao))



